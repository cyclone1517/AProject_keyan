package Bean;
//授权表
public class AuthorizeBean {
	private int role_no;//角色编号
	private int func_no;//功能编号

	public int getRole_no() {
		return role_no;
	}

	public void setRole_no(int role_no) {
		this.role_no = role_no;
	}

	public int getFunc_no() {
		return func_no;
	}

	public void setFunc_no(int func_no) {
		this.func_no = func_no;
	}

}
